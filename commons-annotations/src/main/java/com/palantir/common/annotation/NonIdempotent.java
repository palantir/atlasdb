package com.palantir.common.annotation;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * This annotation is the logical opposite of {@link Idempotent}.
 *
 * Methods that are marked with this annotation should be considered as bugs and fixed as soon as possible.
 *
 */
@Documented
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface NonIdempotent {
    // marker annotation
}
